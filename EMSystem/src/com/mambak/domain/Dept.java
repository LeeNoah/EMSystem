package com.mambak.domain;

public class Dept {
	private String d_no;
	private String d_name;
	private String d_acno;

	public String getD_no() {
		return d_no;
	}

	public void setD_no(String d_no) {
		this.d_no = d_no;
	}

	public String getD_name() {
		return d_name;
	}

	public void setD_name(String d_name) {
		this.d_name = d_name;
	}

	public String getD_acno() {
		return d_acno;
	}

	public void setD_acno(String d_acno) {
		this.d_acno = d_acno;
	}
}
