package com.mambak.domain;

public class Score {
	private String sc_sno;
	private String sc_cno;
	private int sc_score;

	public String getSc_sno() {
		return sc_sno;
	}

	public void setSc_sno(String sc_sno) {
		this.sc_sno = sc_sno;
	}

	public String getSc_cno() {
		return sc_cno;
	}

	public void setSc_cno(String sc_cno) {
		this.sc_cno = sc_cno;
	}

	public int getSc_score() {
		return sc_score;
	}

	public void setSc_score(int sc_score) {
		this.sc_score = sc_score;
	}

}
