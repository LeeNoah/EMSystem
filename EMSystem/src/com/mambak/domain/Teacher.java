package com.mambak.domain;

public class Teacher {
	private String t_no;
	private String t_name;
	private String t_entryyear;
	private String t_password;
	private String t_acno;

	public String getT_no() {
		return t_no;
	}

	public void setT_no(String t_no) {
		this.t_no = t_no;
	}

	public String getT_name() {
		return t_name;
	}

	public void setT_name(String t_name) {
		this.t_name = t_name;
	}

	public String getT_entryyear() {
		return t_entryyear;
	}

	public void setT_entryyear(String t_entryyear) {
		this.t_entryyear = t_entryyear;
	}

	public String getT_password() {
		return t_password;
	}

	public void setT_password(String t_password) {
		this.t_password = t_password;
	}

	public String getT_acno() {
		return t_acno;
	}

	public void setT_acno(String t_acno) {
		this.t_acno = t_acno;
	}

}
